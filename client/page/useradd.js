    isUpdate = false;

    return {
        init(){
            //获取地址栏中问号传参信息，有就是修改，没有就是新增
            let  paramsObj = window.location.href.queryURLParams()   //获取url中所有参数
            if('userId' in paramsObj){
                userId = paramsObj.userId;
                isUpdate = true;
            }



        }
    }









































